def helper(ls):
    my_sum = 0
    neg_num = 0
    pos_num = 0

    for num in ls:
        if int(num) < 0:
            neg_num += 1
        else:
            my_sum += int(num)
            pos_num += 1
    print(neg_num)
    print("%.1f" % (my_sum / pos_num))

while True:
    n = sys.stdin.readline().strip()
    # 还需要这么费劲在这取数
    if n:
        n = n.split(' ')
        # 这种输入多的好像都是用空格分开
        helper(n)
    else:
        break